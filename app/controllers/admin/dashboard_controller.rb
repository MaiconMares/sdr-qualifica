class Admin::DashboardController < Admin::BaseController

  def index
    authorize :admin_dashboard, :index?

    @total_leads = Lead.count
    @total_sdrs = User.sdrs.count
    @active_sdrs = User.active_sdrs.count
    @total_calls = CallSession.finished.count
    @total_imports = Import.count
    @pending_imports = Import.pending.count
    
    @recent_activity = AuditLog.order(created_at: :desc).limit(10)
    
    @statistics = StatisticsService.overall_statistics
  end

  helper_method :activity_icon, :activity_description

