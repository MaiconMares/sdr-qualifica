  def activity_description(log)
    descriptions = {
      call_started: "Chamada iniciada",
      call_ended: "Chamada encerrada",
      status_changed: "Status do lead alterado",
      lead_imported: "Leads importados",
      lead_assigned: "Lead atribuído",
      pause: "Pausa iniciada",
